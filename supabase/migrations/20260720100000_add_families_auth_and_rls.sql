create or replace function app_private.session_context(p_session_token text)
returns table (
  user_id uuid,
  username text,
  app_role text,
  is_admin boolean,
  family_id uuid,
  family_name text,
  member_role text,
  anchor_owner_id uuid,
  expires_at timestamptz,
  needs_password_setup boolean
)
language plpgsql
security definer
set search_path = public, app_private, extensions
as $$
begin
  return query
  select
    u.id,
    u.username::text,
    u.role::text,
    u.is_admin,
    fm.family_id,
    f.name::text,
    fm.member_role::text,
    f.created_by,
    s.expires_at,
    (u.password_hash is null)
  from public.sessions s
  join public.users u on u.id = s.user_id
  left join public.family_members fm on fm.user_id = u.id
  left join public.families f on f.id = fm.family_id
  where s.token::text = p_session_token
    and s.expires_at > now()
  limit 1;

  if not found then
    raise exception 'Invalid or expired session' using errcode = '42501';
  end if;
end;
$$;

revoke all on function app_private.session_context(text) from public, anon, authenticated;

create or replace function public.get_app_session(p_session_token text)
returns jsonb
language plpgsql
security definer
set search_path = public, app_private, extensions
as $$
declare
  ctx record;
begin
  select * into ctx from app_private.session_context(p_session_token);
  return jsonb_build_object(
    'token', p_session_token,
    'expires_at', ctx.expires_at,
    'user_id', ctx.user_id,
    'username', ctx.username,
    'role', ctx.app_role,
    'is_admin', ctx.is_admin,
    'family_id', ctx.family_id,
    'family_name', ctx.family_name,
    'member_role', ctx.member_role,
    'owner_id', ctx.anchor_owner_id,
    'needs_password_setup', ctx.needs_password_setup
  );
exception when sqlstate '42501' then
  return jsonb_build_object('error', 'SESSION_EXPIRED');
end;
$$;

create or replace function public.claim_legacy_account(
  p_session_token text,
  p_email text,
  p_password text
)
returns jsonb
language plpgsql
security definer
set search_path = public, app_private, extensions
as $$
declare
  ctx record;
  clean_email text := lower(btrim(coalesce(p_email, '')));
begin
  select * into ctx from app_private.session_context(p_session_token);

  if not ctx.needs_password_setup then
    return jsonb_build_object('error', 'ALREADY_CONFIGURED');
  end if;
  if clean_email !~ '^[^@[:space:]]+@[^@[:space:]]+\.[^@[:space:]]+$' then
    return jsonb_build_object('error', 'INVALID_EMAIL');
  end if;
  if char_length(coalesce(p_password, '')) < 8 then
    return jsonb_build_object('error', 'WEAK_PASSWORD');
  end if;
  if ctx.is_admin and clean_email <> 'yahavalon76@gmail.com' then
    return jsonb_build_object('error', 'ADMIN_EMAIL_MISMATCH');
  end if;
  if exists (select 1 from public.users where lower(email) = clean_email and id <> ctx.user_id) then
    return jsonb_build_object('error', 'EMAIL_TAKEN');
  end if;

  update public.users
     set email = clean_email,
         password_hash = crypt(p_password, gen_salt('bf', 12)),
         failed_login_attempts = 0,
         locked_until = null
   where id = ctx.user_id;

  update public.sessions
     set expires_at = now() + interval '7 days'
   where token::text = p_session_token;

  return public.get_app_session(p_session_token);
end;
$$;

create or replace function public.register_app_user(
  p_username text,
  p_email text,
  p_password text,
  p_role text,
  p_family_name text default null,
  p_invite_code text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public, app_private, extensions
as $$
declare
  clean_username text := btrim(coalesce(p_username, ''));
  clean_email text := lower(btrim(coalesce(p_email, '')));
  clean_role text := lower(btrim(coalesce(p_role, '')));
  new_user_id uuid := gen_random_uuid();
  target_family_id uuid;
  target_owner_id uuid;
  new_token uuid := gen_random_uuid();
  new_expiry timestamptz := now() + interval '7 days';
  next_invite_code text;
begin
  if char_length(clean_username) not between 2 and 40 then
    return jsonb_build_object('error', 'INVALID_USERNAME');
  end if;
  if clean_email !~ '^[^@[:space:]]+@[^@[:space:]]+\.[^@[:space:]]+$' then
    return jsonb_build_object('error', 'INVALID_EMAIL');
  end if;
  if char_length(coalesce(p_password, '')) < 8 then
    return jsonb_build_object('error', 'WEAK_PASSWORD');
  end if;
  if clean_role not in ('owner', 'shopper') then
    return jsonb_build_object('error', 'INVALID_ROLE');
  end if;
  if exists (select 1 from public.users where lower(btrim(username)) = lower(clean_username)) then
    return jsonb_build_object('error', 'USERNAME_TAKEN');
  end if;
  if exists (select 1 from public.users where lower(email) = clean_email) then
    return jsonb_build_object('error', 'EMAIL_TAKEN');
  end if;

  if clean_role = 'owner' then
    if char_length(btrim(coalesce(p_family_name, ''))) not between 1 and 80 then
      return jsonb_build_object('error', 'FAMILY_NAME_REQUIRED');
    end if;
  else
    select f.id, f.created_by
      into target_family_id, target_owner_id
      from public.families f
     where upper(f.invite_code) = upper(btrim(coalesce(p_invite_code, '')));
    if not found then
      return jsonb_build_object('error', 'INVALID_INVITE');
    end if;
  end if;

  insert into public.users (id, username, email, password_hash, role, shops_for_user_id, is_admin)
  values (
    new_user_id,
    clean_username,
    clean_email,
    crypt(p_password, gen_salt('bf', 12)),
    clean_role,
    case when clean_role = 'shopper' then target_owner_id else null end,
    false
  );

  if clean_role = 'owner' then
    next_invite_code := upper(substr(md5(gen_random_uuid()::text || clock_timestamp()::text), 1, 8));
    insert into public.families (name, invite_code, created_by, legacy_owner_id)
    values (btrim(p_family_name), next_invite_code, new_user_id, new_user_id)
    returning id into target_family_id;
    insert into public.family_members (family_id, user_id, member_role)
    values (target_family_id, new_user_id, 'manager');
  else
    insert into public.family_members (family_id, user_id, member_role)
    values (target_family_id, new_user_id, 'shopper');
  end if;

  insert into public.sessions (token, user_id, expires_at)
  values (new_token, new_user_id, new_expiry);

  return public.get_app_session(new_token::text);
exception when unique_violation then
  return jsonb_build_object('error', 'ACCOUNT_EXISTS');
end;
$$;

create or replace function public.login_app_user(p_username text, p_password text)
returns jsonb
language plpgsql
security definer
set search_path = public, app_private, extensions
as $$
declare
  matched_user public.users%rowtype;
  next_attempts integer;
  new_token uuid := gen_random_uuid();
  new_expiry timestamptz := now() + interval '7 days';
begin
  select * into matched_user
    from public.users
   where lower(btrim(username)) = lower(btrim(coalesce(p_username, '')))
   for update;

  if not found or matched_user.password_hash is null then
    return jsonb_build_object('error', 'INVALID_CREDENTIALS');
  end if;
  if matched_user.locked_until is not null and matched_user.locked_until > now() then
    return jsonb_build_object('error', 'ACCOUNT_LOCKED');
  end if;
  if crypt(coalesce(p_password, ''), matched_user.password_hash) <> matched_user.password_hash then
    next_attempts := matched_user.failed_login_attempts + 1;
    update public.users
       set failed_login_attempts = next_attempts,
           locked_until = case when next_attempts >= 5 then now() + interval '15 minutes' else null end
     where id = matched_user.id;
    return jsonb_build_object('error', case when next_attempts >= 5 then 'ACCOUNT_LOCKED' else 'INVALID_CREDENTIALS' end);
  end if;

  update public.users
     set failed_login_attempts = 0, locked_until = null
   where id = matched_user.id;
  delete from public.sessions where user_id = matched_user.id and expires_at <= now();
  insert into public.sessions (token, user_id, expires_at)
  values (new_token, matched_user.id, new_expiry);

  return public.get_app_session(new_token::text);
end;
$$;

