create or replace function public.add_catalog_food(
  p_session_token text,
  p_name text,
  p_manufacturer text,
  p_category text,
  p_unit_qty text,
  p_picture_url text
)
returns jsonb
language plpgsql
security definer
set search_path = public, app_private
as $$
declare
  ctx record;
  new_food public.foods%rowtype;
begin
  select * into ctx from app_private.session_context(p_session_token);
  if ctx.member_role <> 'manager' and not ctx.is_admin then
    raise exception 'Only household managers can add products' using errcode = '42501';
  end if;
  if char_length(btrim(coalesce(p_name, ''))) = 0
     or char_length(btrim(coalesce(p_manufacturer, ''))) = 0
     or char_length(btrim(coalesce(p_category, ''))) = 0
     or char_length(btrim(coalesce(p_unit_qty, ''))) = 0
     or char_length(btrim(coalesce(p_picture_url, ''))) = 0 then
    raise exception 'All product fields are required' using errcode = '22023';
  end if;
  if exists (
    select 1 from public.foods
     where lower(btrim(name)) = lower(btrim(p_name))
       and lower(btrim(coalesce(manufacturer, ''))) = lower(btrim(p_manufacturer))
       and lower(btrim(coalesce(unit_qty, ''))) = lower(btrim(p_unit_qty))
  ) then
    raise exception 'Product already exists' using errcode = '23505';
  end if;

  insert into public.foods (
    source, external_id, name, manufacturer, unit_qty, picture_url,
    category, created_by, updated_at
  )
  values (
    'manual', 'manual_' || replace(gen_random_uuid()::text, '-', ''),
    btrim(p_name), btrim(p_manufacturer), btrim(p_unit_qty), btrim(p_picture_url),
    btrim(p_category), ctx.user_id, now()
  )
  returning * into new_food;

  return to_jsonb(new_food);
end;
$$;

create or replace function public.update_catalog_food(
  p_session_token text,
  p_food_id uuid,
  p_name text,
  p_manufacturer text,
  p_category text,
  p_unit_qty text,
  p_picture_url text
)
returns jsonb
language plpgsql
security definer
set search_path = public, app_private
as $$
declare
  ctx record;
  existing_food public.foods%rowtype;
begin
  select * into ctx from app_private.session_context(p_session_token);
  select * into existing_food from public.foods where id = p_food_id;
  if not found then
    raise exception 'Product not found' using errcode = 'P0002';
  end if;
  if not ctx.is_admin and existing_food.created_by is distinct from ctx.user_id then
    raise exception 'You can edit only products you added' using errcode = '42501';
  end if;
  if char_length(btrim(coalesce(p_name, ''))) = 0
     or char_length(btrim(coalesce(p_manufacturer, ''))) = 0
     or char_length(btrim(coalesce(p_category, ''))) = 0
     or char_length(btrim(coalesce(p_unit_qty, ''))) = 0
     or char_length(btrim(coalesce(p_picture_url, ''))) = 0 then
    raise exception 'All product fields are required' using errcode = '22023';
  end if;

  update public.foods
     set name = btrim(p_name),
         manufacturer = btrim(p_manufacturer),
         category = btrim(p_category),
         unit_qty = btrim(p_unit_qty),
         picture_url = btrim(p_picture_url),
         updated_at = now()
   where id = p_food_id
   returning * into existing_food;
  return to_jsonb(existing_food);
end;
$$;

create or replace function public.add_receipt_catalog_food(
  p_session_token text,
  p_external_id text,
  p_name text,
  p_manufacturer text,
  p_category text,
  p_unit_qty text,
  p_picture_url text
)
returns jsonb
language plpgsql
security definer
set search_path = public, app_private
as $$
declare
  ctx record;
  saved_food public.foods%rowtype;
  clean_external_id text := nullif(btrim(coalesce(p_external_id, '')), '');
begin
  select * into ctx from app_private.session_context(p_session_token);
  if ctx.member_role <> 'manager' and not ctx.is_admin then
    raise exception 'Only household managers can import receipt products' using errcode = '42501';
  end if;
  if char_length(btrim(coalesce(p_name, ''))) = 0
     or char_length(btrim(coalesce(p_manufacturer, ''))) = 0
     or char_length(btrim(coalesce(p_category, ''))) = 0
     or char_length(btrim(coalesce(p_unit_qty, ''))) = 0 then
    raise exception 'Receipt product data is incomplete' using errcode = '22023';
  end if;

  select * into saved_food
    from public.foods
   where (clean_external_id is not null and external_id = clean_external_id)
      or (
        lower(btrim(name)) = lower(btrim(p_name))
        and lower(btrim(coalesce(manufacturer, ''))) = lower(btrim(p_manufacturer))
        and lower(btrim(coalesce(unit_qty, ''))) = lower(btrim(p_unit_qty))
      )
   order by case when external_id = clean_external_id then 0 else 1 end
   limit 1;
  if found then
    return to_jsonb(saved_food);
  end if;

  insert into public.foods (
    source, external_id, name, manufacturer, unit_qty, picture_url,
    category, created_by, updated_at
  )
  values (
    'receipt',
    coalesce(clean_external_id, 'receipt_' || replace(gen_random_uuid()::text, '-', '')),
    btrim(p_name), btrim(p_manufacturer), btrim(p_unit_qty),
    coalesce(nullif(btrim(coalesce(p_picture_url, '')), ''), '/groceries-app/product-placeholder.svg'),
    btrim(p_category), ctx.user_id, now()
  )
  returning * into saved_food;
  return to_jsonb(saved_food);
end;
$$;

create or replace function public.delete_catalog_food(p_session_token text, p_food_id uuid)
returns boolean
language plpgsql
security definer
set search_path = public, app_private
as $$
declare
  ctx record;
  deleted_food public.foods%rowtype;
begin
  select * into ctx from app_private.session_context(p_session_token);
  if not ctx.is_admin then
    raise exception 'Only the administrator can delete products' using errcode = '42501';
  end if;
  select * into deleted_food from public.foods where id = p_food_id;
  if not found then
    return false;
  end if;
  delete from public.foods where id = p_food_id;
  insert into public.admin_audit_log (actor_user_id, action, entity_type, entity_id, previous_data)
  values (ctx.user_id, 'delete', 'food', p_food_id::text, to_jsonb(deleted_food));
  return true;
end;
$$;

