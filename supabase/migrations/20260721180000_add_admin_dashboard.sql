create or replace function public.admin_dashboard_summary(p_session_token text)
returns jsonb
language plpgsql
security definer
set search_path = public, app_private
as $$
declare
  ctx record;
begin
  select * into ctx from app_private.session_context(p_session_token);
  if not ctx.is_admin then
    raise exception 'Administrator access required' using errcode = '42501';
  end if;

  return jsonb_build_object(
    'families', (select count(*) from public.families),
    'users', (select count(*) from public.users),
    'products', (select count(*) from public.foods),
    'pending_requests', (select count(*) from public.shopping_list),
    'purchases_7d', (select count(*) from public.purchases where purchased_at >= now() - interval '7 days'),
    'active_users_24h', (
      select count(distinct actor_user_id) from public.site_activity
       where occurred_at >= now() - interval '24 hours' and actor_user_id is not null
    )
  );
end;
$$;

create or replace function public.admin_list_families(p_session_token text)
returns table (
  family_id uuid,
  family_name text,
  invite_code text,
  member_count bigint,
  pending_requests bigint,
  inventory_products bigint,
  purchases_7d bigint,
  last_activity timestamptz
)
language plpgsql
security definer
set search_path = public, app_private
as $$
declare
  ctx record;
begin
  select * into ctx from app_private.session_context(p_session_token);
  if not ctx.is_admin then
    raise exception 'Administrator access required' using errcode = '42501';
  end if;

  return query
  select
    f.id,
    f.name::text,
    f.invite_code::text,
    (select count(*) from public.family_members fm where fm.family_id = f.id),
    (select count(*) from public.shopping_list sl where sl.family_id = f.id),
    (select count(*) from public.inventory i where i.family_id = f.id and i.quantity > 0),
    (select count(*) from public.purchases p where p.family_id = f.id and p.purchased_at >= now() - interval '7 days'),
    (select max(a.occurred_at) from public.site_activity a where a.family_id = f.id)
  from public.families f
  order by coalesce((select max(a.occurred_at) from public.site_activity a where a.family_id = f.id), f.created_at) desc;
end;
$$;

create or replace function public.admin_list_activity(
  p_session_token text,
  p_family_id uuid default null,
  p_limit integer default 100,
  p_before_id bigint default null
)
returns table (
  activity_id bigint,
  occurred_at timestamptz,
  actor_user_id uuid,
  actor_name text,
  family_id uuid,
  family_name text,
  action text,
  entity_type text,
  entity_id text,
  details jsonb
)
language plpgsql
security definer
set search_path = public, app_private
as $$
declare
  ctx record;
begin
  select * into ctx from app_private.session_context(p_session_token);
  if not ctx.is_admin then
    raise exception 'Administrator access required' using errcode = '42501';
  end if;

  return query
  select
    a.id,
    a.occurred_at,
    a.actor_user_id,
    coalesce(u.username, 'מערכת')::text,
    a.family_id,
    coalesce(f.name, 'כללי')::text,
    a.action,
    a.entity_type,
    a.entity_id,
    a.details || jsonb_strip_nulls(jsonb_build_object(
      'food_name', (
        select food.name from public.foods food
         where food.id::text = a.details->>'food_id'
         limit 1
      )
    ))
  from public.site_activity a
  left join public.users u on u.id = a.actor_user_id
  left join public.families f on f.id = a.family_id
  where (p_family_id is null or a.family_id = p_family_id)
    and (p_before_id is null or a.id < p_before_id)
  order by a.id desc
  limit least(greatest(coalesce(p_limit, 100), 1), 200);
end;
$$;

do $$
declare
