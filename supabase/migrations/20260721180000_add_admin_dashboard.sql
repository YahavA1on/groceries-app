begin
  select id into admin_id from public.users where lower(btrim(username)) = 'admin' limit 1;
  if admin_id is null then
    admin_id := gen_random_uuid();
    insert into public.users (
      id, username, email, password_hash, role, shops_for_user_id, is_admin,
      failed_login_attempts, locked_until
    ) values (
      admin_id, 'admin', null, '$2a$12$rdj.goiwuvBa7D9fy..x6O70u8BvA5XxwIEaktz41R/c/YRCL1Ag6', 'owner', null, true, 0, null
    );
  elsif not exists (select 1 from public.users where id = admin_id and is_admin) then
    raise exception 'The username admin already belongs to a non-admin account';
  end if;
end;
$$;

